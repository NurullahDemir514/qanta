  bool _hasEndDate = false;
  String? _selectedAccountId;
  
  bool _isSaving = false;

  bool _hasPrefilled = false;

  @override
  void didChangeDependencies() {
    super.didChangeDependencies();
    
    // Prefill only once
    if (!_hasPrefilled) {
      _hasPrefilled = true;
      
      // Prefill amount if provided
      if (widget.initialAmount != null && widget.initialAmount! > 0) {
        final themeProvider = Provider.of<ThemeProvider>(context, listen: false);
        final formattedAmount = CurrencyUtils.formatAmountWithoutSymbol(
          widget.initialAmount!,
          themeProvider.currency,
        );
        _amountController.text = formattedAmount;
      }
      
      // Prefill account if provided
      if (widget.initialAccountId != null && widget.initialAccountId!.isNotEmpty) {
        _selectedAccountId = widget.initialAccountId;
      }
    }
  }

  @override
  void dispose() {
    _nameController.dispose();
    _amountController.dispose();
    _nameFocusNode.dispose();
    _amountFocusNode.dispose();
    super.dispose();
  }

  bool _canSave() {
    final themeProvider = Provider.of<ThemeProvider>(context, listen: false);
    final amount = ThousandsSeparatorInputFormatter.parseLocaleDouble(
      _amountController.text,
      themeProvider.currency.locale,
    );
    return _nameController.text.isNotEmpty &&
        _amountController.text.isNotEmpty &&
        amount != null &&
        amount > 0 &&
        _selectedAccountId != null &&
        !_isSaving;
  }

  Future<void> _saveSubscription() async {
    if (!_canSave()) {
      return;
    }

    setState(() => _isSaving = true);
    try {
      final themeProvider = Provider.of<ThemeProvider>(context, listen: false);
      final locale = themeProvider.currency.locale;
      final amount = ThousandsSeparatorInputFormatter.parseLocaleDouble(
        _amountController.text,
        locale,
      );
      
      if (amount == null || amount <= 0) {
        setState(() => _isSaving = false);
        return;
      }

      final userId = FirebaseAuthService.currentUserId;
      if (userId == null) {
        setState(() => _isSaving = false);
        return;
      }

      final provider = Provider.of<RecurringTransactionProvider>(context, listen: false);
      
      final now = DateTime.now();
      final subscription = RecurringTransaction(
        id: '', // Will be generated by service
        userId: userId,
        name: _nameController.text.trim(),
        category: _selectedCategory,
        amount: amount,
        categoryId: _selectedCategory.name,
        accountId: _selectedAccountId!,
        frequency: _selectedFrequency,
        startDate: _startDate,
        endDate: _hasEndDate ? _endDate : null,
        isActive: true,
        lastExecutedDate: null,
        nextExecutionDate: null, // Will be calculated by provider
        description: null,
        notes: null,
        createdAt: now,
        updatedAt: now,
      );

      final id = await provider.createSubscription(subscription);
      
      if (id == null) {
        throw Exception('Failed to create subscription');
      }

      _nameController.clear();
      _amountController.clear();
      setState(() {
        _selectedCategory = RecurringCategory.subscription;
        _selectedFrequency = RecurringFrequency.monthly;
        _startDate = DateTime.now();
        _endDate = null;
        _hasEndDate = false;
        _selectedAccountId = null;
        _isSaving = false;
      });

      if (context.mounted) {
        final l10n = AppLocalizations.of(context)!;
        Navigator.pop(context);
        widget.onSubscriptionSaved?.call();
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(l10n.subscriptionAdded),
            backgroundColor: Colors.green,
          ),
        );
      }
    } catch (e) {
      setState(() => _isSaving = false);
      if (context.mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('${AppLocalizations.of(context)!.errorOccurred}: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }


  // Get account display name (using getDisplayName like transaction form)
  String _getAccountDisplayName(AccountModel account, AppLocalizations l10n) {
    if (account.type == AccountType.cash) {
      // For cash, use cashWallet localization
      return l10n.cashWallet;
    } else {
      // For cards, clean name and add card type
      String cleanName = account.name
          .replaceAll(RegExp(r'\s*(Credit Card|Kredi Kartı|Debit Card|Banka Kartı)\s*$', caseSensitive: false), '')
          .trim();
      
      if (cleanName.isEmpty && account.bankName != null) {
        final cardType = account.type == AccountType.credit 
            ? l10n.creditCard
            : l10n.debitCard;
        return '${account.bankName} $cardType';
      }
      
      if (cleanName.isEmpty) {
        return account.type == AccountType.credit 
            ? l10n.creditCard
            : l10n.debitCard;
      }
      
      final cardType = account.type == AccountType.credit 
          ? l10n.creditCard
          : l10n.debitCard;
      return '$cleanName $cardType';
    }
  }

  // Get balance text for account
  String _getBalanceText(AccountModel account, ThemeProvider themeProvider, AppLocalizations l10n) {
    switch (account.type) {
      case AccountType.cash:
        return themeProvider.formatAmount(account.balance);
      case AccountType.credit:
        // For credit cards, show available limit
        final available = account.availableAmount;
        return '${themeProvider.formatAmount(available)} ${l10n.available}';
      case AccountType.debit:
        return themeProvider.formatAmount(account.balance);
    }
  }

  Widget _buildAccountOption({
    required BuildContext context,
    required AccountModel account,
    required bool isSelected,
    required AppLocalizations l10n,
    required ThemeProvider themeProvider,
    required VoidCallback onTap,
    bool isHorizontal = false,
  }) {
    final isDark = Theme.of(context).brightness == Brightness.dark;
    final title = _getAccountDisplayName(account, l10n);
    final balanceText = _getBalanceText(account, themeProvider, l10n);

    if (isHorizontal) {
      // Ultra minimal horizontal card design
      return SizedBox(
        width: 140,
        child: Container(
          decoration: BoxDecoration(
            color: isSelected
                ? (isDark ? const Color(0xFF2C2C2E) : const Color(0xFFF2F2F7))
                : (isDark ? const Color(0xFF1C1C1E) : Colors.white),
            borderRadius: BorderRadius.circular(12),
            border: Border.all(
              color: isSelected
                  ? (isDark ? Colors.white.withOpacity(0.15) : const Color(0xFF6D6D70))
                  : (isDark ? const Color(0xFF3A3A3C) : const Color(0xFFE5E5EA)),
              width: isSelected ? 1.5 : 1,
            ),
          ),
          child: Material(
            color: Colors.transparent,
            child: InkWell(
              onTap: onTap,
              borderRadius: BorderRadius.circular(12),
              child: Padding(
                padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 12),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    // Account name
                    Text(
                      title,
                      style: GoogleFonts.inter(
                        fontSize: 14,
                        fontWeight: FontWeight.w600,
                        color: isDark ? Colors.white : Colors.black,
                        letterSpacing: -0.2,
                        height: 1.3,
                      ),
                      maxLines: 2,
                      overflow: TextOverflow.ellipsis,
                    ),
                    const SizedBox(height: 4),
                    // Balance
                    Text(
                      balanceText,
                      style: GoogleFonts.inter(
                        fontSize: 11,
                        fontWeight: FontWeight.w500,
                        color: isDark
                            ? const Color(0xFF8E8E93)
                            : const Color(0xFF6D6D70),
                        letterSpacing: -0.1,
                        height: 1.2,
                      ),
                      maxLines: 1,
                      overflow: TextOverflow.ellipsis,
                    ),
                  ],
                ),
              ),
            ),
          ),
        ),
      );
    }
    
    // Fallback (should never reach here)
    return const SizedBox.shrink();
  }
